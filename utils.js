// -- Addition Box --
export function getSum(firstNum, secondNum) {
    return firstNum + secondNum;
};

// -- Subtraction Box --
export function getDiff(firstNum, secondNum) {
    return firstNum - secondNum;
};

// -- Multiply Box --
export function getProduct(firstNum, secondNum) {
    return firstNum * secondNum;
};

// -- Division Box --
export function getQuotient(firstNum, secondNum) {
    return firstNum / secondNum;
};